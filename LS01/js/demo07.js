//布尔类型与逻辑运算符

//逻辑与、或的基本理解
console.log(2>1&&4<5);
//答：true
console.log(true&&(!2));
//答：false
console.log(false&&("2" == 2));
//答：false
console.log(false&&false);
//答：false
console.log(2>1||4<5);
//答：true
console.log(true||(!2));
//答：true
console.log(false||("2" == 2));
//答：true
console.log(false||false);
//答：false